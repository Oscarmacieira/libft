#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	ch;

	ch = (char)c;
	i = ft_strlen(s) + 1;
	while (--i >= 0)
	{
		if (s[i] == ch)
			return ((char *)s + i);
	}
	return (NULL);
}